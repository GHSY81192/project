<script>
  $("#submit").click(function(){
       var  j =[];
     $("#sign").find('input').each(function(){
        if($(this).val()==''){

           $(this).parent().siblings('span').css('display','block');
             return false;
        }else{

          if($(this).attr('type')=='tel'){

            if(!(/^1[34578]\d{9}$/.test($(this).val()))){
              $(this).parent().siblings('span').css('display','block');
              return false;
            }else{
              j.push($(this).attr('name')+'='+$(this).val());
              $("#modalCnt").css('display','none');
              var k= j.join('&');
              var code =$('#code').val();
              var wxuserid =$('#wxuserid').val();
                     k = k+'&code='+code+'&wxuserid='+wxuserid;
              $.ajax({
                type:"POST",
                url :"/phone/Activity/signUp",
                data:k,
                dataType:'json',
                success:function(data){
                     if(data.status==1){
                       $("#modalCnt2").css('display','block').find('div[class=modal-dialog]').css('top',"200px");

                       $("#ui-btn-enlist1").css('display',"block");
                       $("#ui-btn-enlist2").css('display',"none");
                     }else{

                       $("#modalCnt3").css('display','block').find('div[class=modal-dialog]').css('top',"200px").find('div[class=modal-bd]').text(data.msg);
                     }
                }
              })

            }
          }else{
            j.push($(this).attr('name')+'='+$(this).val());
          }

        }

     })

  })
  $("#sign").find('input').each(function(){

    $(this).on('focus',function(){
       $(this).parent().siblings('span').css('display','none');
    })

  })
</script>
